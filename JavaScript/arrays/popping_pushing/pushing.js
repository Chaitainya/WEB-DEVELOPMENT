// Pushing

// The push() method adds a new element to an array (at the end):

var marshal_arts = ["Karate", "Boxing", "Krav Maga"];

marshal_arts.push("Kick Boxing");

console.log(marshal_arts); // [ "Karate", "Boxing", "Krav Maga", "Kick Boxing" ]

marshal_arts.push(["Kung Fu", "Taekwondo", "Muay Thai"]);

console.log(marshal_arts);