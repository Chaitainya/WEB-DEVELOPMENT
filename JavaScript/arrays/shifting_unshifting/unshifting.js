// The unshift() method adds a new element to an array (at the beginning):

var fav_heroines = ["Kajal", "Jacquiline", "Rhea", "Puja Hedge"];

fav_heroines.unshift("Puja Gupta");

console.log(fav_heroines); // [ 'Puja Gupta', 'Kajal', 'Jacquiline', 'Rhea', 'Puja Hedge' ]

fav_heroines.unshift(["Amanda Cery", "Monica Sharma", "Nora Fatehi"]);

console.log(fav_heroines);